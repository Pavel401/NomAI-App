class AnimatedScannerOverlay extends StatefulWidget {
  final bool
      isBarcode; // Determines whether to use barcode or food scanner shape
  final Color borderColor;
  final double borderWidth;
  final Color overlayColor;
  final double borderRadius;
  final double borderLength;
  final double? cutOutWidth;
  final double? cutOutHeight;
  final double cutOutBottomOffset;
  final Duration animationDuration;

  const AnimatedScannerOverlay({
    Key? key,
    this.isBarcode = false,
    this.borderColor = Colors.blue,
    this.borderWidth = 3.0,
    this.overlayColor = const Color.fromRGBO(0, 0, 0, 80),
    this.borderRadius = 12,
    this.borderLength = 40,
    this.cutOutWidth,
    this.cutOutHeight,
    this.cutOutBottomOffset = 0,
    this.animationDuration = const Duration(milliseconds: 1500),
  }) : super(key: key);

  @override
  State<AnimatedScannerOverlay> createState() => _AnimatedScannerOverlayState();
}

class _AnimatedScannerOverlayState extends State<AnimatedScannerOverlay>
    with SingleTickerProviderStateMixin {
  late AnimationController _animationController;
  late Animation<Color?> _colorAnimation;
  late Animation<double> _borderWidthAnimation;

  @override
  void initState() {
    super.initState();

    _animationController = AnimationController(
      vsync: this,
      duration: widget.animationDuration,
    );

    _colorAnimation = ColorTween(
      begin: widget.borderColor,
      end: widget.borderColor.withOpacity(0.3),
    ).animate(
      CurvedAnimation(
        parent: _animationController,
        curve: Curves.easeInOut,
      ),
    );

    _borderWidthAnimation = TweenSequence<double>([
      TweenSequenceItem(
        tween: Tween<double>(
            begin: widget.borderWidth, end: widget.borderWidth * 1.5),
        weight: 1,
      ),
      TweenSequenceItem(
        tween: Tween<double>(
            begin: widget.borderWidth * 1.5, end: widget.borderWidth),
        weight: 1,
      ),
    ]).animate(
      CurvedAnimation(
        parent: _animationController,
        curve: Curves.easeInOut,
      ),
    );

    _animationController.repeat(reverse: true);
  }

  @override
  void dispose() {
    _animationController.dispose();
    super.dispose();
  }

  @override
  Widget build(BuildContext context) {
    return AnimatedBuilder(
      animation: _animationController,
      builder: (context, child) {
        return Container(
          decoration: ShapeDecoration(
            shape: widget.isBarcode
                ? BarcodeScannerOverlayShape(
                    borderColor: _colorAnimation.value!,
                    borderWidth: _borderWidthAnimation.value,
                    overlayColor: widget.overlayColor,
                    borderRadius: widget.borderRadius,
                    borderLength: widget.borderLength,
                    cutOutWidth: widget.cutOutWidth,
                    cutOutHeight: widget.cutOutHeight,
                    cutOutBottomOffset: widget.cutOutBottomOffset,
                  )
                : FoodScannerOverlayShape(
                    borderColor: _colorAnimation.value!,
                    borderWidth: _borderWidthAnimation.value,
                    overlayColor: widget.overlayColor,
                    borderRadius: widget.borderRadius,
                    borderLength: widget.borderLength,
                    cutOutWidth: widget.cutOutWidth,
                    cutOutHeight: widget.cutOutHeight,
                    cutOutBottomOffset: widget.cutOutBottomOffset,
                  ),
          ),
        );
      },
    );
  }
}
